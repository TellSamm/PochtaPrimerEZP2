package Uzichaem_Java;

public class primer_if {
    public static void main(String[] args) {
        int x = 2;
        if (x == 3){
            System.out.println("у нас сейчас равняется 3");
        }else{
            System.out.println("x не равен сейчас 3");
        }
        System.out.println("Эта строка выполнится в любом случае в конце условия if!");
    }
}
